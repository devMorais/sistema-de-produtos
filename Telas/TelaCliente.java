    private void bt_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_alterarActionPerformed
        // Alterar cliente
        if (tabela_clientes.getSelectedRow() > -1) {
            if (tf_nome.getText().trim().length() > 0 && tf_idade.getText().trim().length() > 0
                    && tf_sobrenome.getText().trim().length() > 0 && tf_fone.getText().trim().length() > 0) {

                int escolha = JOptionPane.showConfirmDialog(null, "Deseja alterar?");
                if (escolha == 0) {
                    Banco.ClienteSQL altera = new Banco.ClienteSQL();
                    altera.cliente.setNome(tf_nome.getText());
                    altera.cliente.setIdade(Integer.parseInt(tf_idade.getText()));
                    altera.cliente.setSobrenome(tf_sobrenome.getText());
                    altera.cliente.setFone(tf_fone.getText());
                    altera.cliente.setId_cliente(Integer.parseInt(tabela_clientes.getValueAt(tabela_clientes.getSelectedRow(), 0).toString()));
                    if (altera.AlterarCliente() > 0) {
                        JOptionPane.showMessageDialog(null, "Dados do cliente alterado com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Erro ao alterar cliente!");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Cancelado pelo usuário!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "É preciso editar para alterar!!");
            }

        } else {
            JOptionPane.showMessageDialog(null, "É preciso selecionar um cliente em consulta cliente para alterar!!");
        }
        new Utilitario().LimpaTela(jPanel2);
        bt_salvar.setEnabled(true);
        cb_tipo.setVisible(true);
        desctipo.setVisible(true);
        bt_editar.setEnabled(true);


    }//GEN-LAST:event_bt_alterarActionPerformed

