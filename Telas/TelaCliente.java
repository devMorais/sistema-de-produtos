    private void bt_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluirActionPerformed
        // Excluir
        if (tabela_clientes.getSelectedRow() > -1) {
            int escolha = JOptionPane.showConfirmDialog(null, "Deseja excluir?");

            if (escolha == 0) {

                Banco.ClienteSQL msql = new Banco.ClienteSQL();
                msql.cliente.setId_cliente(Integer.parseInt(tabela_clientes.getValueAt(tabela_clientes.getSelectedRow(), 0).toString()));
                if (msql.ExcluirCliente() > 0) {

                    JOptionPane.showMessageDialog(null, "Cliente excluída com sucesso!");

                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir cliente!");

                }
            } else {
                JOptionPane.showMessageDialog(null, "Cancelado pelo usuário!");
            }

        } else {
            JOptionPane.showMessageDialog(null, "É preciso selecionar um cliente em consulta de cliente para excluir!");
        }

        new Utilitario().LimpaTela(jPanel2);
        bt_salvar.setEnabled(true);
        cb_tipo.setVisible(true);
        desctipo.setVisible(true);
        bt_editar.setEnabled(true);
    }//GEN-LAST:event_bt_excluirActionPerformed

