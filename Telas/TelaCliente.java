    public void carregadados() {
        try {
            bd.getConnection();
            sql = "Select id_cliente ID, nome NOME, sobrenome SOBRENOME, idade IDADE, fone TELEFONE from cliente";
            st = bd.connection.prepareStatement(sql);
            rs = st.executeQuery();

            DefaultTableModel tableModel = new DefaultTableModel(new String[]{}, 0) {
                public boolean isCellEditable(int rowIndex, int mColIndex) {
                    return false;
                }

            };

            int qtdeColunas = rs.getMetaData().getColumnCount();

            for (int indice = 1; indice <= qtdeColunas; indice++) {
                tableModel.addColumn(rs.getMetaData().getColumnLabel(indice));
            }

            tabela_clientes = new JTable(tableModel);
            DefaultTableModel dtm = (DefaultTableModel) tabela_clientes.getModel();

            while (rs.next()) {
                try {
                    String[] dados = new String[qtdeColunas];
                    for (int i = 1; i <= qtdeColunas; i++) {
                        dados[i - 1] = rs.getString(i);
                    }
                    dtm.addRow(dados);
                    System.out.println();
                } catch (Exception erro) {

                }
                jScrollPane1.setViewportView(tabela_clientes);
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro);
        }

    }

