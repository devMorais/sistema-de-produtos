    public class ItemCliente {

        public String id, descricao, sobrenome;

        public ItemCliente(String id, String descricao, String sobrenome) {
            this.id = id;
            this.descricao = descricao;
            this.sobrenome = sobrenome;
        }

        public String toString() {
            return descricao + " " + sobrenome;
        }
    }

    public class ItemProd {

        public String id, descricao;

        public ItemProd(String id, String descricao) {
            this.id = id;
            this.descricao = descricao;
        }

        public String toString() {
            return descricao;
        }
    }

    public void Carregacbcliente() {
        sql = "select id_cliente, nome, sobrenome from cliente";
        try {
            conexao.getConnection();
            st = conexao.connection.prepareStatement(sql);
            rs = st.executeQuery();
            while (rs.next()) {
                String id = rs.getString(1);
                String descricao = rs.getString(2);
                String sobrenome = rs.getString(3);
                cb_cliente.addItem(new TelaVendas.ItemCliente(id, descricao, sobrenome));
            }
        } catch (Exception erro) {
        }
    }

    public void Carregacbproduto() {
        sql = "select id_prod, desc_marca from produto";
        try {
            conexao.getConnection();
            st = conexao.connection.prepareStatement(sql);
            rs = st.executeQuery();
            while (rs.next()) {
                String id = rs.getString(1);
                String descricao = rs.getString(2);

                cb_produto.addItem(new TelaVendas.ItemProd(id, descricao));
            }
        } catch (Exception erro) {
        }
    }

    public void remover(int linha) {
        ((DefaultTableModel) tabelaCarrinho.getModel()).removeRow(linha);
    }

