    private void bt_adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_adicionarActionPerformed
        // Botão add itens
        if (tf_quantidade.getText().trim().length() > 0 && tf_valor.getText().trim().length() > 0) {

            ItemProd i = (ItemProd) cb_produto.getSelectedItem();
            qtd = Integer.parseInt(tf_quantidade.getText());
            preco = Double.parseDouble(tf_valor.getText());
            subtotal = qtd * preco;
            total += subtotal;
            tf_totalVenda.setText(String.valueOf(total));
            //Adicionar o produto no carrinho
            carrinho = (DefaultTableModel) tabelaCarrinho.getModel();
            carrinho.addRow(new Object[]{
                i.id,
                i.descricao,
                tf_quantidade.getText(),
                tf_valor.getText(),
                subtotal
            });
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos!!");
        }
    }//GEN-LAST:event_bt_adicionarActionPerformed

    private void bt_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_removerActionPerformed
        // Remover venda por linha
        if (tabelaCarrinho.getRowCount() > 0) {
            if (tabelaCarrinho.getSelectedRow() > -1) {
                int escolha = JOptionPane.showConfirmDialog(null, "Deseja remover do carrinho?");
                if (escolha == 0) {
                    subtotal = Double.parseDouble(tabelaCarrinho.getValueAt(tabelaCarrinho.getSelectedRow(), 4).toString());
                    total -= subtotal;
                    tf_totalVenda.setText(String.valueOf(total));
                    remover(tabelaCarrinho.getSelectedRow());
                    tf_valor.setText("");
                    tf_quantidade.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Cancelado pelo usuário!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma venda!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seu carrinho de vendas está vazio!");
        }
    }//GEN-LAST:event_bt_removerActionPerformed

    private void bt_esvaziarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_esvaziarActionPerformed
        // Esvaziar carrinho
        if (tabelaCarrinho.getRowCount() > 0) {
            int escolha = JOptionPane.showConfirmDialog(null, "Deseja esvaziar o carrinho?");
            if (escolha == 0) {
                int linhas = tabelaCarrinho.getModel().getRowCount();
                for (int i = linhas - 1; i >= 0; i--) {
                    remover(i);
                    tf_totalVenda.setText("");
                    tf_valor.setText("");
                    tf_quantidade.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cancelado pelo usuário!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seu carrinho de vendas está vazio!");
        }
    }//GEN-LAST:event_bt_esvaziarActionPerformed

    private void bt_finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_finalizarActionPerformed
        // Finalizar Vendas
        if (tabelaCarrinho.getRowCount() > 0) {
            int escolha = JOptionPane.showConfirmDialog(null, "Deseja confirmar todas as vendas?");
            if (escolha == 0) {
                int linhas = tabelaCarrinho.getModel().getRowCount();
                for (int i = linhas - 1; i >= 0; i--) {
                    Banco.VendaSQL vendas = new Banco.VendaSQL();
                    ItemCliente ic = (ItemCliente) cb_cliente.getSelectedItem();
                    vendas.venda.setCliente_id_cliente(Integer.parseInt(ic.id.toString()));
                    vendas.venda.setProduto_id_prod(Integer.parseInt(tabelaCarrinho.getValueAt(i, 0).toString()));
                    vendas.venda.setQtd(Integer.parseInt(tabelaCarrinho.getValueAt(i, 2).toString()));
                    vendas.venda.setValor(Double.parseDouble(tabelaCarrinho.getValueAt(i, 3).toString()));
                    try {
                        vendas.InsereVendas();
                        remover(i);
                        tf_totalVenda.setText("");
                        tf_valor.setText("");
                        tf_quantidade.setText("");
                    } catch (Exception erro) {
                    }
                }
                JOptionPane.showMessageDialog(null, "Vendas efetuadas com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Cancelado pelo usuário!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seu carrinho de vendas está vazio!");
        }

    }//GEN-LAST:event_bt_finalizarActionPerformed

